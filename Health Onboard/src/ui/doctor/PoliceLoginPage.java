//           new PoliceDashboardTest().setVisible(true);
//       }
//       else
//           JOptionPane.showMessageDialog(null,"Incorrect Username or Password");
        
        
        String userName = txtUserName.getText();
        String password = txtPassword.getText();
        String userType = cbUserType.getSelectedItem().toString();
       
        try {
                Connection con=SQLConnect.Connect();
                PreparedStatement st=con.prepareStatement("Select UserId, UserName, Password, UserType from LoginCredentials where UserName = '"+userName+"' and Password='"+password+"' and UserType = '"+userType+"'");
                
                //st.setString(1, str_user);
                //st.setString(2, str_pswrd);
                ResultSet rs=st.executeQuery();
                if(rs.next()) {
                   //int id=rs.getString("id");
                    JOptionPane.showMessageDialog(null,"Successfully logged in");
                    if(userType.equals("Police Admin")){
                        PoliceAdminDashboard ad = new PoliceAdminDashboard();
                        ad.setVisible(true);
                    }
                    if (userType.equals("Police Staff")){
                        PoliceDashboard ad = new PoliceDashboard();
                        ad.setVisible(true);
                    //new Page(int_id);
                    
                    }this.setVisible(false);
                }else{
                        JOptionPane.showMessageDialog(null,"Incorrect details");
                    }
            }
            
        catch(Exception ex){
        JOptionPane.showMessageDialog(null,ex.toString(),"Error",JOptionPane.ERROR_MESSAGE);
        } 
